CREATE PROCEDURE PROCESS_INVOICE (
    INVOICE_ID INTEGER,
    DISCOUNT   NUMERIC(5,2)
)
RETURNS (
    STATUS_CODE INTEGER,
    STATUS_MSG  VARCHAR(100)
)
AS
DECLARE VARIABLE SUBTOTAL NUMERIC(12,2);
DECLARE VARIABLE TAX      NUMERIC(12,2);
DECLARE VARIABLE APPLY    INTEGER;
BEGIN
    SELECT AMOUNT, TAX_RATE INTO :SUBTOTAL, :TAX
    FROM INVOICES
    WHERE ID = :INVOICE_ID;

    IF (:SUBTOTAL IS NULL) THEN BEGIN
        STATUS_CODE = -1;
        STATUS_MSG  = 'Invoice not found';
        SUSPEND;
        EXIT;
    END

    IF (:DISCOUNT > 0) THEN BEGIN
        IF (:SUBTOTAL > 500) THEN BEGIN
            EXECUTE PROCEDURE CALC_DISCOUNT(:SUBTOTAL, :DISCOUNT) RETURNING_VALUES :APPLY;
            SUBTOTAL = :SUBTOTAL - :APPLY;
        END ELSE BEGIN
            SUBTOTAL = :SUBTOTAL - (:SUBTOTAL * :DISCOUNT / 100);
        END
    END

    STATUS_CODE = 0;
    STATUS_MSG  = 'OK';
    SUSPEND;
END
