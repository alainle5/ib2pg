namespace IB2PG;

public enum TokenType
{
    // Literals and identifiers
    Identifier,
    QuotedIdentifier,
    StringLiteral,
    IntegerLiteral,
    FloatLiteral,

    // Colon-prefixed variable reference — colon stripped in Value
    ColonIdent,

    // Punctuation
    LeftParen,
    RightParen,
    Comma,
    Semicolon,
    Dot,
    Equals,
    NotEquals,
    LessThan,
    GreaterThan,
    LessEqual,
    GreaterEqual,
    Plus,
    Minus,
    Star,
    Slash,
    Concatenate,

    // Keywords
    KwCreate,
    KwProcedure,
    KwReturns,
    KwAs,
    KwBegin,
    KwEnd,
    KwDeclare,
    KwVariable,
    KwIf,
    KwThen,
    KwElse,
    KwFor,
    KwSelect,
    KwFrom,
    KwWhere,
    KwInto,
    KwDo,
    KwSuspend,
    KwExecute,
    KwWhile,
    KwExit,
    KwException,
    KwWhen,
    KwNot,
    KwAnd,
    KwOr,
    KwNull,
    KwIs,
    KwIn,
    KwLike,
    KwInsert,
    KwUpdate,
    KwDelete,
    KwSet,
    KwValues,
    KwInteger,
    KwVarchar,
    KwChar,
    KwDate,
    KwTimestamp,
    KwFloat,
    KwDouble,
    KwPrecision,
    KwNumeric,
    KwDecimal,
    KwSmallint,
    KwBigint,
    KwBlob,
    KwBoolean,
    KwJoin,
    KwInner,
    KwLeft,
    KwRight,
    KwOuter,
    KwOn,
    KwOrder,
    KwBy,
    KwGroup,
    KwHaving,
    KwDistinct,
    KwAll,
    KwReturningValues,

    // Special
    EndOfFile,
    Unknown
}

public sealed record Token(
    TokenType Type,
    string RawText,
    string Value,
    int Line,
    int Column);
